package day4_looping;

public class PrintStar {

	public static void main(String[] args) {
     for(int first_line=0;first_line<=0;first_line++) {
    	 {
    		 System.out.println("*");
    	 }
    	 for(int second_line=0;second_line<=1;second_line++)
    	 { 
    		 System.out.print("*");
    	 }
    	 System.out.println();
    	 for(int thirt_line=0;thirt_line<=2;thirt_line++)
    	 {
    		 System.out.print("*");
    	 }
    	 System.out.println();
    	 for(int fourth_line=0;fourth_line<=3;fourth_line++)
    	 {
    		 System.out.print("*");
    	 }
    	 System.out.println();
    	 for(int fifth_line=0;fifth_line<=4;fifth_line++)
    	 {
    		 System.out.print("*");
    	 }
     }

	}

}
