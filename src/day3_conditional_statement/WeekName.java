package day3_conditional_statement;

public class WeekName {

	public static void main(String[] args) {
		String week_name= "mon";
		switch(week_name) {
		case "sun":System.out.println("day 1"); break;
		case "mon":System.out.println("day 2"); break;
		case "tue":System.out.println("day 3"); break;
		case "wed":System.out.println("day 4"); break;
		case "thu":System.out.println("day 5"); break;
		case "fri":System.out.println("day 6"); break;
		default :System.out.println("day 7"); 

	}

}}
